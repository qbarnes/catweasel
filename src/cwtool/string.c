/****************************************************************************
 * string_equal
 ****************************************************************************/
int
string_equal(
	const char			*src1,
	const char			*src2)

	{
	while ((*src1 | *src2) != '\0') if (*src1++ != *src2++) return (0);
	return (1);
	}



/****************************************************************************
 * string_length
 ****************************************************************************/
int
string_length(
	const char			*src)

	{
	int				i = 0;

	while (src[i] != '\0') i++;
	return (i);
	}



