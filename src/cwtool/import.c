/****************************************************************************
 * import_ushort_be
 ****************************************************************************/
int
import_ushort_be(
	unsigned char			*data)

	{
	int				val = data[0];

	val = (val << 8) | data[1];
	return (val);
	}



/****************************************************************************
 * import_ushort_le
 ****************************************************************************/
int
import_ushort_le(
	unsigned char			*data)

	{
	int				val = data[1];

	val = (val << 8) | data[0];
	return (val);
	}



/****************************************************************************
 * import_ulong_le
 ****************************************************************************/
unsigned long
import_ulong_le(
	unsigned char			*data)

	{
	unsigned long			val = data[3];

	val = (val << 8) | data[2];
	val = (val << 8) | data[1];
	val = (val << 8) | data[0];
	return (val);
	}
