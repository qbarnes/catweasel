#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>



/****************************************************************************
 * main
 ****************************************************************************/
int
main(
	void)

	{
	unsigned char			buffer[0x10000];
	int				i, j = 0, r;

	while (1)
		{
		r = read(STDIN_FILENO, buffer, sizeof (buffer));
		if (r == -1)
			{
			perror("fwdump");
			exit(1);
			}
		if (r == 0) break;
		for (i = 0; i < r; i++)
			{
			printf("0x%02x,", buffer[i]);
			if ((++j % 16) == 0) printf("\n");
			}
		}
	if ((j % 16) != 0) printf("\n");
	fflush(stdout);
	return (0);
	}
